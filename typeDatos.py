# String
myStr="hello word"
print("Hola Mundo")
print(myStr.upper()) #Mayusculas
print(myStr.lower()) #Minuscula
print(myStr.swapcase()) #
print(myStr.capitalize()) #Primera mayuscula
print(myStr.replace('hello','bye')) #Remplace de texto
print(myStr.count('l')) # Contador de letras 
print(myStr.startswith("hola")) # Preguntar si inicia cuenta con la palabra True/False
print(myStr.endswith("hola")) # Preguntar si termina cuenta con la palabra True/False
print(myStr.split()) # return ['hello','world´]
print(myStr.split(',')) # return ['hello','world´]
print(myStr.find('o')) # posición 4
print(len(myStr))  # conteo de la longitud
print(myStr.index('e')) # el index donde esta la letra 1
print(myStr[0]) # Pinta la posición  especifica H
